export function add(a, b) {
  return a + b;
}

export function minus(a, b) {
  //.................วิธีแบบเก่า................... //
  return a - b;
}

export function multiply(a, b) {
  return a * b;
}

export const divide = (a, b) => a / b; //.................วิธีแบบใหม่................... //

export const pp = (a, b) => a % b;
